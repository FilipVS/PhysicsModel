namespace ModelDisplay
{
    /// <summary>
    /// The types of particles available
    /// </summary>
    public enum ParticleTypes
    {
        /// <summary>
        /// Stationary particle with positive charge
        /// </summary>
        StationaryPositive = 0,

        /// <summary>
        /// Stationary particle with negative charge
        /// </summary>
        StationaryNegative,

        /// <summary>
        /// Movable particle with positive charge
        /// </summary>
        MovablePositive,

        /// <summary>
        /// Movable particle with negative charge
        /// </summary>
        MovableNegative
    }
}
